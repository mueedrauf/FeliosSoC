`timescale 1ns / 1ps
////////////////////////////////////////////////////////////////////////////////
// Module : tb_dmem_wb_slave
// Unit testbench for dmem_wb_slave (64 KB Wishbone data SRAM).
// Optimized for Vivado XSIM.
////////////////////////////////////////////////////////////////////////////////
module tb_dmem_wb_slave;

    logic        clk, rst;
    logic        wb_cyc, wb_stb, wb_we;
    logic [3:0]  wb_sel;
    logic [31:0] wb_adr, wb_dat_m;
    logic [31:0] wb_dat_s;
    logic        wb_ack;

    // Clock Generation (100 MHz)
    initial clk = 0;
    always #5 clk = ~clk;

    // Device Under Test (DUT)
    dmem_wb_slave dut (
        .wb_clk_i(clk),      .wb_rst_i(rst),
        .wb_cyc_i(wb_cyc),   .wb_stb_i(wb_stb), .wb_we_i(wb_we),
        .wb_adr_i(wb_adr),   .wb_dat_i(wb_dat_m), .wb_sel_i(wb_sel),
        .wb_dat_o(wb_dat_s), .wb_ack_o(wb_ack)
    );

    // ?? Scoreboard ??????????????????????????????????????????????????????????
    int pass_cnt = 0, fail_cnt = 0;

    task automatic chk(input string name, input logic [31:0] got, exp);
        if (got === exp) begin
            $display("  [PASS] %-45s  got=%08X", name, got);
            pass_cnt++;
        end else begin
            $display("  [FAIL] %-45s  got=%08X  exp=%08X", name, got, exp);
            fail_cnt++;
        end
    endtask

    // ?? WB write transaction ????????????????????????????????????????????????
    task automatic wb_write(
        input logic [31:0] addr,
        input logic [31:0] data,
        input logic [3:0]  sel
    );
        @(posedge clk);
        wb_cyc   <= 1; 
        wb_stb   <= 1; 
        wb_we    <= 1;
        wb_adr   <= addr; 
        wb_dat_m <= data; 
        wb_sel   <= sel;

        // Wait for ACK active
        do begin
            @(posedge clk);
        end while (!wb_ack);

        // Deassert cycles clean on next edge
        wb_cyc   <= 0; 
        wb_stb   <= 0; 
        wb_we    <= 0;
        wb_adr   <= 32'h0; 
        wb_dat_m <= 32'h0; 
        wb_sel   <= 4'b0000;
    endtask

    // ?? WB read transaction ?????????????????????????????????????????????????
    task automatic wb_read(
        input  logic [31:0] addr,
        output logic [31:0] data
    );
        @(posedge clk);
        wb_cyc   <= 1; 
        wb_stb   <= 1; 
        wb_we    <= 0;
        wb_adr   <= addr; 
        wb_sel   <= 4'b1111;

        // Wait for ACK active
        do begin
            @(posedge clk);
        end while (!wb_ack);

        #1; // Minor settling delay to securely capture structural driven data
        data     = wb_dat_s;

        wb_cyc   <= 0; 
        wb_stb   <= 0;
        wb_adr   <= 32'h0;
        wb_sel   <= 4'b0000;
    endtask

 task automatic check_ack_latency;
        int cycles;
        
        // 1. Drive inputs on negedge to ensure they are stable for the next posedge
        @(negedge clk);
        wb_cyc = 1; wb_stb = 1; wb_we = 0;
        wb_adr = 32'h0002_0008; wb_sel = 4'b1111;
        cycles = 0;

        // 2. Wait for the clock edge where the slave samples the stable inputs
        @(posedge clk);
        
        // 3. Wait for the clock edge where the slave asserts ACK
        @(posedge clk);
        cycles = 1; // It is exactly 1 clock cycle latency

        // 4. Sample signal (Give the simulator a tiny delta to propagate)
        #1; 

        $display("  [INFO] ACK latency: %0d clock cycle(s) after CYC+STB", cycles);
        if (wb_ack) begin
            $display("  [PASS] Single-cycle ACK confirmed"); 
            pass_cnt++;
        end else begin
            $display("  [FAIL] Expected 1-cycle ACK, got >1"); 
            fail_cnt++;
        end

        // 5. Clean up
        @(negedge clk);
        wb_cyc = 0; wb_stb = 0; wb_adr = 32'h0;
    endtask
    // ?????????????????????????????????????????????????????????????????????????
    // Main Simulation Execution
    // ?????????????????????????????????????????????????????????????????????????
    logic [31:0] rd_data;

    initial begin
        $display("");
        $display("????????????????????????????????????????????????????????????");
        $display("?  dmem_wb_slave Unit Testbench                            ?");
        $display("????????????????????????????????????????????????????????????");

        // Initialization state setup
        wb_cyc = 0; wb_stb = 0; wb_we = 0;
        wb_sel = 4'b1111; wb_adr = 0; wb_dat_m = 0;
        rst = 1;
        repeat(4) @(posedge clk);
        rst = 0;
        repeat(2) @(posedge clk);

        // ?? TEST 1: Word write / word read ???????????????????????????????????
        $display(""); $display("??? TEST 1: Word write/read ??????????????????????????????");
        wb_write(32'h0002_0000, 32'hDEAD_BEEF, 4'b1111);
        wb_read (32'h0002_0000, rd_data);
        chk("Word R/W addr 0x0000: 0xDEADBEEF", rd_data, 32'hDEAD_BEEF);

        wb_write(32'h0002_0004, 32'hCAFE_BABE, 4'b1111);
        wb_read (32'h0002_0004, rd_data);
        chk("Word R/W addr 0x0004: 0xCAFEBABE", rd_data, 32'hCAFE_BABE);

        wb_write(32'h0002_0100, 32'h1234_5678, 4'b1111);
        wb_read (32'h0002_0100, rd_data);
        chk("Word R/W addr 0x0100: 0x12345678", rd_data, 32'h1234_5678);

        // ?? TEST 2: Byte-enable partial write ????????????????????????????????
        $display(""); $display("??? TEST 2: Byte-enable partial write ????????????????????");
        // Write full word first: 0xAABBCCDD
        wb_write(32'h0002_0008, 32'hAABB_CCDD, 4'b1111);
        // Overwrite only low 2 bytes with 0x1122 (sel=0011)
        wb_write(32'h0002_0008, 32'h0000_1122, 4'b0011);
        wb_read (32'h0002_0008, rd_data);
        // Expected: [31:16]=0xAABB (unchanged), [15:0]=0x1122 (overwritten)
        chk("Byte-enable sel=0011: [31:16]=0xAABB [15:0]=0x1122", rd_data, 32'hAABB_1122);

        // Overwrite only high byte with 0xFF (sel=1000)
        wb_write(32'h0002_0008, 32'hFF00_0000, 4'b1000);
        wb_read (32'h0002_0008, rd_data);
        // Expected: [31:24]=0xFF, [23:0]=0xBB1122
        chk("Byte-enable sel=1000: [31:24]=0xFF  [23:0]=0xBB1122", rd_data, 32'hFFBB_1122);

        // ?? TEST 3: Single-cycle ACK latency ?????????????????????????????????
        $display(""); $display("??? TEST 3: ACK latency ??????????????????????????????????");
        check_ack_latency();

        // ?? TEST 4: Address boundary (last word of 64 KB) ????????????????????
        $display(""); $display("??? TEST 4: Address boundary (64 KB - 4) ????????????????");
        // Last valid word: base 0x0002_0000 + 0xFFFC = 0x0002_FFFC
        wb_write(32'h0002_FFFC, 32'hFEED_FACE, 4'b1111);
        wb_read (32'h0002_FFFC, rd_data);
        chk("Last word (0xFFFC): 0xFEEDFACE", rd_data, 32'hFEED_FACE);

        // ?? TEST 5: Reset does not corrupt stored data ????????????????????????
        $display(""); $display("??? TEST 5: SRAM survives reset (ACK logic resets only) ??");
        wb_write(32'h0002_0010, 32'h5A5A_5A5A, 4'b1111);
        rst = 1;
        repeat(4) @(posedge clk);
        rst = 0;
        repeat(2) @(posedge clk);
        wb_read(32'h0002_0010, rd_data);
        chk("After reset: SRAM[0x10] still = 0x5A5A5A5A", rd_data, 32'h5A5A_5A5A);

        // ?? TEST 6: Back-to-back transactions ????????????????????????????????
        $display(""); $display("??? TEST 6: Back-to-back transactions ????????????????????");
        wb_write(32'h0002_0020, 32'h0000_0001, 4'b1111);
        wb_write(32'h0002_0024, 32'h0000_0002, 4'b1111);
        wb_write(32'h0002_0028, 32'h0000_0003, 4'b1111);
        wb_read(32'h0002_0020, rd_data); chk("Back2back [0x20]=1", rd_data, 32'd1);
        wb_read(32'h0002_0024, rd_data); chk("Back2back [0x24]=2", rd_data, 32'd2);
        wb_read(32'h0002_0028, rd_data); chk("Back2back [0x28]=3", rd_data, 32'd3);

        // ?? Summary ???????????????????????????????????????????????????????????
        $display("");
        $display("???????????????????????????????????????????????????????????");
        if (fail_cnt == 0)
            $display("  ALL %0d TESTS PASSED", pass_cnt);
        else
            $display("  FAILED: %0d / %0d", fail_cnt, pass_cnt+fail_cnt);
        $display("???????????????????????????????????????????????????????????");
        $finish;
    end

    // Watchdog Timer
    initial begin #500_000; $display("[WATCHDOG] timeout"); $finish; end

endmodule